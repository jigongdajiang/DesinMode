public class Description {
    /**
     * 定义:
     *      把一个类的接口变成客户端所期待的另一种接口，从而使原本因为接口不匹配而无法在一起工作的两个类能在一起工作
     * 场景:
     *      任何一个问题都可以加一个中间层来解决
     *      有两种类型:类适配器，对象适配器(建议使用的类型，因为是组合)
     * 角色:
     *      目标(Target)角色:目标接口
     *      原(Adapee)角色:需要适配的原接口
     *      适配器(Adapter)角色
     * 案例:
     *      变压器
     * Android:
     *      ListView  与 BaseAdapter
     * 利弊:
     *      利:复用扩展
     *      弊:过多的适配器不易理解
     */
}
